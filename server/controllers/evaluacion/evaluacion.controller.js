exports.evaluacionesXSemanaListar = async (req, res) => {
    try {
    db.Evaluacion.listarEvaluacionesXSemana(req,(err,data)=>{
            if(err){
                res.json({
                    success: false,
                    error: {
                        "message": err.message
                    }
                });
                return;
            }
            console.log(data);
            if(data==null){
                res.json({
                    success: false,
                    Evaluacion: [],
                }); 
                return;
            }
            res.json({
                success: true,
                Evaluacion: data,
            }); 
    }) 
    } catch (errorTRY) {
        console.log(errorTRY)
        res.json({
            success: false,
            error: {
                "message": "Error en el servidor"
            },
            "message": "Error en el servidor"
        });
    }
}

